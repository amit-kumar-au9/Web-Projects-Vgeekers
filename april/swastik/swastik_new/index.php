    $('.send-otp').on('click', function(){
      var mobile = $('input[name="mobile"]').val();
      var name = $('input[name="name"]').val();
      var selectClass = $('select[name="class"]').val();
      if((mobile != '' && mobile != NaN && mobile != null && mobile != undefined) && (name != '' && name != NaN && name != null && name != undefined) && (selectClass != '' && selectClass != NaN && selectClass != null && selectClass != undefined)){
        $.ajax({
          'type' : 'post',
          'url' : 'send-otp.php',
          'data' : 'mobile='+mobile,
          success : function(response){
            var json = JSON.parse(response);
            if(!json.error){
              $('div.e-msg-otp').addClass('success').removeClass('error').text(json.message);
              $('.submit-btn').show();
              $('#otp-field').show();
            }
            else{
              $('div.e-msg-otp').addClass('error').removeClass('success').text(json.message);
              $('.submit-btn').hide();
              $('#otp-field').hide();
            }
          }
        });
      }
      else{
        $('div.e-msg-otp').addClass('error').removeClass('success').text('Please Enter All the Details');
      }
    });

